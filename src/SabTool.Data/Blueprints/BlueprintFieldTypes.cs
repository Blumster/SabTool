    private static Dictionary<Type, Func<byte[], int, bool, object?>> Readers { get; } = new();

    static BlueprintFieldTypes()
    {
        static Func<byte[], int, bool, object?> ReaderWithSizeCheck(int expectedSize, Func<byte[], int, object?> reader)
        {
            return (data, off, skipCheck) =>
            {
                if (!skipCheck && data.Length != expectedSize)
                    throw new Exception($"Data is expected to be of size {expectedSize}, but found size: {data?.Length ?? -1}");

                return reader(data, off);
            };
        }

        Readers.Add(typeof(bool), ReaderWithSizeCheck(1, (data, off) => data[off] != 0));
        Readers.Add(typeof(sbyte), ReaderWithSizeCheck(1, (data, off) => (sbyte)data[off]));
        Readers.Add(typeof(byte), ReaderWithSizeCheck(1, (data, off) => data[off]));
        Readers.Add(typeof(short), ReaderWithSizeCheck(2, (data, off) => BitConverter.ToInt16(data, off)));
        Readers.Add(typeof(ushort), ReaderWithSizeCheck(2, (data, off) => BitConverter.ToUInt16(data, off)));
        Readers.Add(typeof(int), ReaderWithSizeCheck(4, (data, off) => BitConverter.ToInt32(data, off)));
        Readers.Add(typeof(uint), ReaderWithSizeCheck(4, (data, off) => BitConverter.ToUInt32(data, off)));
        Readers.Add(typeof(long), ReaderWithSizeCheck(8, (data, off) => BitConverter.ToInt64(data, off)));
        Readers.Add(typeof(ulong), ReaderWithSizeCheck(8, (data, off) => BitConverter.ToUInt64(data, off)));
        Readers.Add(typeof(float), ReaderWithSizeCheck(4, (data, off) => BitConverter.ToSingle(data, off)));
        Readers.Add(typeof(double), ReaderWithSizeCheck(8, (data, off) => BitConverter.ToDouble(data, off)));
        Readers.Add(typeof(Vector2), ReaderWithSizeCheck(8, (data, off) => new Vector2(BitConverter.ToSingle(data, off), BitConverter.ToSingle(data, off + 4))));
        Readers.Add(typeof(Vector3), ReaderWithSizeCheck(12, (data, off) => new Vector3(BitConverter.ToSingle(data, off), BitConverter.ToSingle(data, off + 4), BitConverter.ToSingle(data, off + 8))));
        Readers.Add(typeof(Vector4), ReaderWithSizeCheck(16, (data, off) => new Vector4(BitConverter.ToSingle(data, off), BitConverter.ToSingle(data, off + 4), BitConverter.ToSingle(data, off + 8), BitConverter.ToSingle(data, off + 12))));
        Readers.Add(typeof(Crc), ReaderWithSizeCheck(4, (data, off) => new Crc(BitConverter.ToUInt32(data, off))));
        Readers.Add(typeof(Color), ReaderWithSizeCheck(4, (data, off) => Color.FromArgb(data[off + 3], data[off], data[off + 1], data[off + 2])));
        Readers.Add(typeof(LuaParam), (data, off, _) =>
        {
            using var reader = new BinaryReader(new MemoryStream(data, off, data.Length - off, false));

            return new LuaParam(reader);
        });
        Readers.Add(typeof(string), (data, off, _) =>
        {
            if (data.Length == 0)
                return string.Empty;

            var str = Encoding.UTF8.GetString(data, off, data.Length - off);

            var nullTerminatorIndex = str.IndexOf('\0');
            if (nullTerminatorIndex == -1)
                return str;

            if (nullTerminatorIndex == 0)
                return string.Empty;

            return str[..nullTerminatorIndex];
        });

        var toAdd = new HashSet<BlueprintType>();

        while (true)
        {
            var didSomething = false;

            foreach (var entry in Parents)
            {
                entry.Value.Add(BlueprintType.Common);
                entry.Value.Add(BlueprintType.Unknown);

                foreach (var parent in entry.Value)
                {
                    if (!Parents.ContainsKey(parent))
                        continue;

                    foreach (var parentVal in Parents[parent])
                        toAdd.Add(parentVal);
                }

                var startCount = entry.Value.Count;

                foreach (var newEntry in toAdd)
                    entry.Value.Add(newEntry);

                toAdd.Clear();

                didSomething = didSomething || startCount != entry.Value.Count;
            }

            if (!didSomething)
                break;
        }
    }

    private static int GetTypeSize(Type type) => type.Name switch
    {
        "Boolean" or "Byte" or "SByte" => 1,
        "Int16" or "UInt16" => 2,
        "Int32" or "UInt32" or "Single" or "Crc" or "Color" => 4,
        "Int64" or "UInt64" or "Double" or "Vector2" => 8,
        "Vector3" => 12,
        "Vector4" => 16,
        _ => -1,
    };

    public static object? ReadProperty(BlueprintType blueprintType, Property prop)
    {
        var realBlueprintType = GetRealBlueprintType(blueprintType, prop);

        var key = (realBlueprintType, prop.Name.Value);

        if (PropertyTypes.TryGetValue(key, out var propType))
        {
            if (propType is null)
                throw new Exception($"Encountered null type for key: ({key.realBlueprintType}, 0x{key.Value:X8})!");

            if (propType.IsArray)
            {
                var readType = propType.GetElementType();

                var elementSize = GetTypeSize(readType);
                if (elementSize == -1)
                    throw new Exception($"Type {readType} cannot be read as an array!");

                var elementCount = prop.Data.Length / elementSize;
                var array = Array.CreateInstance(readType, elementCount);

                for (var i = 0; i < elementCount; ++i)
                    array.SetValue(ReadTypedValue(readType, prop.Data, true, i * elementSize), i);

                return array;
            }

            return ReadTypedValue(propType, prop.Data);
        }

        return null;
    }

    public static HashSet<BlueprintType>? GetAllTypes(BlueprintType type)
    {
        return Parents.TryGetValue(type, out var allTypes) ? allTypes : null;
    }

    public static BlueprintType GetRealBlueprintType(BlueprintType blueprintType, Property prop)
    {
        if (!PropertyTypes.ContainsKey((blueprintType, prop.Name.Value)))
        {
            var allTypes = GetAllTypes(blueprintType);

            foreach (var parentType in allTypes)
                if (PropertyTypes.ContainsKey(new(parentType, prop.Name.Value)))
                    return parentType;
        }

        return blueprintType;
    }

    private static object? ReadTypedValue(Type type, byte[] data, bool skipCheck = false, int off = 0) => Readers.TryGetValue(type, out var readerFunc) ? readerFunc(data, off, skipCheck) : null;

